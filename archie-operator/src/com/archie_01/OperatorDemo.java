package com.archie_01;
// 算术运算符
public class OperatorDemo {
    public static void main(String[] args) {
//        定义两个 int 类型的变量
        int a = 6;
        int b = 4;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
//        整数相除只能得到整数 要想得到小数, 必须要有浮点数的参与
        System.out.println(6.0 / 4);
    }
}
